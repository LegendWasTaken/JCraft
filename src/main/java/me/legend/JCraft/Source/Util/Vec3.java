package me.legend.JCraft.Source.Util;

public class Vec3 {

    private int x, y, z;

    public Vec3(int x, int y, int z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() { return this.x; }
    public int getY() { return this.y; }
    public int getZ() { return this.z; }

}
